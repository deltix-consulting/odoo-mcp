def test_existing_config_cancel_does_nothing(
    fake_paths: Path,
    monkeypatch: pytest.MonkeyPatch,
    capsys: pytest.CaptureFixture[str],
) -> None:
    cfg_path = fake_paths / "config.toml"
    cfg_path.write_text(_SAMPLE_CONFIG)
    os.chmod(cfg_path, 0o600)

    sentinel: dict[str, Any] = {"doctor": 0, "scan": 0}

    def _doctor_count() -> int:
        sentinel["doctor"] += 1
        return 0

    def _scan_count(name: str) -> tuple[int, str]:
        sentinel["scan"] += 1
        return 0, ""

    monkeypatch.setattr(onboarding_cli, "_run_doctor", _doctor_count)
    monkeypatch.setattr(onboarding_cli, "_run_scan", _scan_count)
    _stdin(monkeypatch, ["", "3"])  # cancel

    rc = onboarding_cli.main([])
    assert rc == 0
    assert sentinel["doctor"] == 0
    assert sentinel["scan"] == 0
    out = capsys.readouterr().out
    assert "Cancelled" in out


